    let { modalHistory, history, popout } = this.state;

    if (popout !== null) {
      this.setState({ popout: null });
      window.history.pushState({ pop: "popout" }, "Title");
      return;
    }

    if (modalHistory.length > 0) {
      this.modalBack();
      return;
    }

